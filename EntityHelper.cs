        private static Dictionary<string, string> formClassToEntity;

        private static void InitFormClassToEntity()
        {
            formClassToEntity = new Dictionary<string, string>();

            // items podle konfigurace
            var items = FORM_CLASS_TO_ENTITY.Split('\r', '\n').Where(x => x.Contains("=")).Select(item => item.Split('='));
            foreach (var formclass_entity in items)
            {
                if (formclass_entity.Length != 2) continue;
                var key = formclass_entity[0];
                var value = formclass_entity[1];
                formClassToEntity[key] = value;
            }

            // items formclass co nemaji entity -> tem jsem manualne vymyslel
            var manualItems = FORM_CLASS_TO_ENTITY_MANUALLY.Split('\r', '\n').Where(x => x.Contains("=")).Select(item => item.Split('='));
            foreach (var formclass_entity in manualItems)
            {
                if (formclass_entity.Length != 2) continue;
                var key = formclass_entity[0];
                var value = formclass_entity[1];
                formClassToEntity[key] = value;
            }
        }
        
        static EntityHelper()
        {
            InitFormClassToEntity();
        }

        /// <summary>
        /// Maps a form class to its corresponding entity.
        /// </summary>
        /// <param name="formClass">The form class to map.</param>
        /// <returns>The entity corresponding to the form class, or null if no mapping exists.</returns>
        public static string FormClassToEntity(string formClass)
        {
            return string.IsNullOrEmpty(formClass) ?  formClass : 
                   formClassToEntity.TryGetValue(formClass, out var entity) ? entity : null;
        }
